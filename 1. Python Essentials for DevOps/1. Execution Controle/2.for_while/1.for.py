'''
for loops allow you to repeat a block of statements (a code block) once for each member of a sequence (ordered group of items). 
As you iterate through the sequence, the current item can be accessed by the code block. One of most common uses of
loops is to iterate through a range object to do a task a set number of times:
'''

for i in range(10):
    x = i*2
    print(x)
    